

parrot = "Norwegian Blue"

print(parrot)

# Print first character
print(parrot[0])

# To print up to 4th character not including 4th character
# By default counting starts from 0
print(parrot[:4])

# this is same as print(parrot[:4])
print(parrot[0:4])

# start and end position
print(parrot[3:7])

# Extract character from end position
print(parrot[-1])
print(parrot[-3])

# start position to end
print(parrot[6:])

# backward start and End
# this is starts from -4 th character and end at up to index character not including the mentioned index
print(parrot[-4:-1])
print(parrot[-4:-2])
print(parrot[-4:-3])


# by default interval is 1 but it can be changed
print(parrot[1:6:2])
print(parrot[1:8:2])

